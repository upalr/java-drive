
/**
 * Interface for representing common geometic Object
 *
 * @author Upal Roy
 * @version 1.0.0
 */
public interface GeometricObject
{
    /**
     * method signature - get the perimeter of gemotric Object
     *
     * @return  double it returns the perimeter of geometric object
     */
    double getPerimeter();
    
    /**
     * method signature - get the area of gemotric Object
     *
     * @return  double it returns the area of geometric object
     */
    double getArea();
}
